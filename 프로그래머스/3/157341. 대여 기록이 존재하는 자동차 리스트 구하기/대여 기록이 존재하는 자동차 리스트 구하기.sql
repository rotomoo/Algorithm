SELECT
CAR_RENTAL_COMPANY_RENTAL_HISTORY.CAR_ID
from CAR_RENTAL_COMPANY_RENTAL_HISTORY
join CAR_RENTAL_COMPANY_CAR
on CAR_RENTAL_COMPANY_CAR.CAR_ID = CAR_RENTAL_COMPANY_RENTAL_HISTORY.CAR_ID
where CAR_RENTAL_COMPANY_CAR.CAR_TYPE = '세단'
and month(CAR_RENTAL_COMPANY_RENTAL_HISTORY.START_DATE) = 10
group by CAR_RENTAL_COMPANY_RENTAL_HISTORY.CAR_ID
order by CAR_RENTAL_COMPANY_RENTAL_HISTORY.CAR_ID desc